                            <p>When you <a href="https://www.bluehost.com/blog/how-to-create-a-website-guide/">create a
                                    website</a>,
                                “About Us” pages are one of those must-haves that you cannot neglect. Every website has
                                one, from big
                                eCommerce brands to someone’s blog. It provides an opportunity for visitors to get to
                                know your
                                business. With a compelling “About Us” page, you can immediately establish connections
                                with people who
                                landed on your site. There is no hard structure to it, and it’s not always easy to know
                                what to include
                                in such a section. In this post, I’ll go into detail about what information you should
                                consider
                                including, the dos and don’ts, and how to set up an About section in WordPress.</p>



                            <h2 id="h-why-do-you-need-an-about-us-page"><span class="ez-toc-section"
                                    id="Why_do_you_need_an_About_Us_page"
                                    ez-toc-data-id="#Why_do_you_need_an_About_Us_page"></span>Why do you need an About
                                Us page?<span class="ez-toc-section-end"></span></h2>



                            <p>So, people have landed on your site. They already saw the homepage where you introduce
                                your blog,
                                products, or services. Now they want to find more information about the person or people
                                behind the
                                products or services you offer. This is when they look for your “About Us” page – to
                                know more about
                                you! The page can go by different names – “About”, “Our story” – but their purpose is
                                the same. To tell
                                visitors who you are, and why they should choose your business!</p>



                            <p>When visitors first land on your site, you’re a stranger to them. Use this page to
                                explain to them what
                                it is that you do, and why you do it. They should be presented with clear information
                                about you or your
                                business.</p>



                            <p>Don’t think of this section as an obligation and only write a few paragraphs here and
                                there. This is a
                                wonderful opportunity for you to share your stories, values and build trust between you
                                and your site
                                visitors. When people know who you are, what you do, what you believe in, they are more
                                likely to trust
                                and do business with you.</p>



                            <h2 id="h-what-information-should-you-include-on-your-about-us-page"><span
                                    class="ez-toc-section"
                                    id="What_information_should_you_include_on_your_About_Us_page"
                                    ez-toc-data-id="#What_information_should_you_include_on_your_About_Us_page"></span>What
                                information
                                should you include on your About Us page?<span class="ez-toc-section-end"></span></h2>



                            <p>There are so many ways you can go about telling your company story. It’s tough to
                                establish a
                                one-size-fits-all structure for an About page. In general, try to think about what you
                                want visitors to
                                know about you, and what you think they want to know. In addition to that, your brand
                                personality should
                                speak loud and clear here. Let visitors know whom they are doing business with!</p>



                            <p>The following list is just a suggestion of the various information you could have on your
                                “About Us”
                                page, regardless of the purpose of your website. You don’t need to include all of it on
                                your page.
                                Choose the information that you think is suitable for your business.</p>



                            <h3 id="h-your-story">Your story</h3>



                            <p>Every business has a story to tell. We believe an “About” page should always include a
                                piece of the story
                                about you or your company. This is especially true for bloggers and smaller companies.
                                People love
                                brands with personalities nowadays. And stories are powerful mediums for you to connect
                                with your
                                audience. People tell stories. It’s what we do as humans!</p>



                            <p>How did you come up with the idea of this blog or this company? When did you decide to
                                pursue this idea?
                                How has it grown during all those times? Don’t be afraid to summarize and share such
                                experiences in a
                                piece of writing. Try to encapsulate your history and development in just a few
                                paragraphs. You don’t
                                have to be a book author to write one, and you certainly don’t need to make it lengthy.
                            </p>



                            <p>You should definitely mention the “Aha!” moment that led to the creation of your blog,
                                products, or
                                services. In addition to that, you may want to include the important milestones that you
                                or your company
                                achieve over time. Milestones are your company’s history, so be proud of them! And who
                                knows, you might
                                inspire some visitors with your compelling story and history.</p>



                            <h3 id="h-your-company-mission">Your company mission</h3>



                            <p>Your mission statement is an action-based statement that declares the purpose of your
                                business and how
                                you serve your customers. This sometimes includes a description of the company, what it
                                does, and its
                                objectives. A mission statement must answer the big three questions: the “what,” the
                                “who,” and the
                                “why,” of your company. Therefore, your mission statement is basically a condensed
                                guideline by which
                                your company operates.</p>



                            <p>Most mission statements are between one and three sentences. It should be easy to
                                understand, and easy to
                                remember. After the mission statement, it’s also good to explain how you go about
                                achieving such a
                                mission. How do your company operations fit in with your mission?</p>



                            <p>For instance, <a href="https://www.google.com/search/howsearchworks/mission/"
                                    target="_blank" rel="noreferrer noopener">Google’s mission statement</a> is: “to
                                organize the world’s information
                                and make it universally accessible and useful”. It is short and nicely summarizes what
                                the people at
                                Google do. Then they go on to talk about how they do it: by managing, innovating, and
                                improving their
                                search function.</p>



                            <h3 id="h-your-team">Your team</h3>



                            <p>Your people are what makes your company! Let’s face it, your company wouldn’t exist
                                without the people
                                behind it. Use this section to briefly introduce them to your visitors. You don’t have
                                to introduce
                                every single one of them. But it’s good to include some of the important people such as
                                the founders and
                                management team here.</p>



                            <p>In addition to that, you can also introduce some common qualities and values that your
                                team members
                                share. And if you can, do include photos of your team, and even photos of the individual
                                team members.
                                This helps to create a sense of “human” for your visitors. As humans, we tend to feel
                                more connected to
                                people when we see them. It’s also a good idea to create a dedicated page about your
                                team and put a link
                                in this section.</p>



                            <h3 id="h-introduce-your-values">Introduce your values</h3>



                            <p>Customers want to be treated like human beings. For that to happen, they need to feel
                                that they’re
                                communicating with human beings. Be sure to describe who you are as a person or as a
                                team, and what your
                                personal values are. What do you believe in? What’s your company culture like? What’s
                                your contribution
                                to the greater picture – meaning your community and the world around you?</p>



                            <h3 id="h-other-things-you-may-want-to-consider">Other things you may want to consider</h3>



                            <p>Got some loyal customers who’ve been with you for quite a while? If the answer is “yes”,
                                then you may
                                want to show them off to your visitors. There must be a reason why these people
                                continuously do business
                                with you, right? Loyal customers can give great testimony to why other people should buy
                                your products
                                or services.</p>



                            <p>If your business has been given an award, be proud of it! In fact, show it to your
                                visitors. Awards from
                                credited sources show your credibility to the world. And credibility is what invites
                                people to trust
                                you. Make sure not to go overboard with it, and don’t boast too much about your
                                achievements. Just a few
                                lines of text, photos, or icons may do the job.</p>



                            <p>Some small businesses also introduce their work process on their About page. Although you
                                should have
                                separate pages which explain how you work – for small businesses, this is okay as well.
                                If you want to
                                include your work process in the about section, be sure not to explain everything there.
                                Just give a
                                brief explanation and pair the copy with photos and graphics to make your content more
                                appealing.</p>



                            <h2 id="h-what-you-should-and-shouldn-t-do-on-your-about-us-page"><span
                                    class="ez-toc-section"
                                    id="What_you_should_and_shouldn%E2%80%99t_do_on_your_About_Us_page"
                                    ez-toc-data-id="#What_you_should_and_shouldn’t_do_on_your_About_Us_page"></span>What
                                you should and
                                shouldn’t do on your About Us page<span class="ez-toc-section-end"></span></h2>



                            <h3 id="h-the-do-s">The Do’s</h3>



                            <p>1: Show off your brand’s personality, whether it’s through communication style (your
                                copywriting style,
                                tone of voice), or visual elements such as colors, logos, imagery.</p>



                            <p>2: Make a brilliant and engaging design for your page! Invite visitors to click on your
                                content by giving
                                it a nice look. Make sure to distribute your content nicely so it’s easier for visitors
                                to read and
                                navigate. Don’t scare them away with blocks after blocks of text. Leave some white
                                spaces in your design
                                so visitors are not flooded with information. White spaces also allow your text and
                                images to shine.</p>



                            <p>3: Do use compelling photography and illustrations that demonstrate your brand’s
                                personality. Imagery is
                                a crucial factor for any brand. You can really connect with your visitors by selecting
                                images that speak
                                to them.</p>



                            <p>4: Including videos here could be a great idea. Especially if you have videos that you
                                think will be
                                interesting for your visitors, or you just want to show off other cool sides of your
                                company! Just don’t
                                go overboard and include something like your year-end party recap.</p>



                            <p>5: Create subpages for different content and link them to your About Us page. This will
                                help keep your
                                page cleaner. Let’s say you support a charity fund with your business. That’s an
                                incredibly good thing
                                and you should let the world know on your About page. Instead of giving a long
                                explanation of what you
                                do with the charity fund in this section, you can briefly mention it and link to a
                                subpage where people
                                can find more information.</p>



                            <p>6: Your copywriting and content should be on point here. Don’t write lengthy text, and
                                also don’t make it
                                too short to the point that people think you don’t care enough. Next to that, your text
                                should be
                                compelling, clear, and easy to understand.</p>



                            <p>If you want to read more about what you should add to your About Us page, check out our
                                blog post on <a
                                    href="https://www.bluehost.com/resources/10-must-haves-for-your-about-page/">10
                                    must-have elements
                                    for your company’s about page</a>.</p>



                            <h3>The Don’ts</h3>



                            <p>1: Don’t push sales here! This depends on the business and industry that you operate in.
                                But in most
                                cases, don’t try too hard to sell to your visitors. Visitors probably saw all the
                                information about your
                                product on the homepage already. This section is about you connecting with your
                                visitors!</p>



                            <p>2: Don’t boast too much about your business to visitors! They probably already know
                                you’re good at
                                something when they get to your site. Be authentic and be truthful in what you
                                communicate.</p>



                            <p>3: Don’t put too much information in this section. It’s easy to write too much,
                                especially about
                                something you’re passionate about. Keep your information concise, straight to the point.
                            </p>



